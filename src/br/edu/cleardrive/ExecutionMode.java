package br.edu.cleardrive;

/**
 * Enum to determine the execution mode.
 *
 * @version 2.0.0
 *
 * @author Clederson Cruz
 *
 */
public enum ExecutionMode {

	/**
	 * NONE: this software is not running yet.
	 */
	NONE,

	/**
	 * CLI: this software is running in command line mode.
	 */
	CLI,

	/**
	 * GUI: this software is running in graphical mode.
	 */
	GUI
}
